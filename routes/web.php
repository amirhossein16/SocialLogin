Route::get('/', function () {
    return view('welcome');
});
Route::controller(\App\Http\Controllers\SocialController::class)->group(function(){
    Route::get('auth/facebook', 'facebookRedirect')->name('auth.facebook');
    Route::get('auth/facebook/callback', 'loginWithFacebook');
});

Route::controller(\App\Http\Controllers\GoogleController::class)->group(function(){
    Route::get('auth/google', 'redirectToGoogle')->name('auth.google');
    Route::get('auth/google/callback', 'handleGoogleCallback');
});

Route::middleware([
    'auth:sanctum',
    config('jetstream.auth_session'),
    'verified'
])->group(function () {
    Route::get('/dashboard', function () {
        return view('dashboard');
    })->name('dashboard');
});
